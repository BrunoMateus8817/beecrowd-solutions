A = int(input())
B = int(input())
SOMA = A + B
print(f"SOMA = {SOMA}")
# Este programa lê dois números inteiros, calcula a soma e imprime o resultado no formato "SOMA = resultado".
# A função `input()` é usada para ler os números, e a função `print()`  
# exibe o resultado formatado. A variável `SOMA` armazena a soma dos dois números.
# O uso de `f-string` permite incluir a variável diretamente na string de saída.