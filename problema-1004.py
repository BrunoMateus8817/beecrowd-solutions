# Problema 1004 - Produto Simples (Beecrowd)
# Este programa lê dois inteiros, calcula o produto e exibe no formato "PROD = resultado"

a = int(input())
b = int(input())

prod = a * b

print(f"PROD = {prod}")
# A função `input()` é usada para ler os números inteiros.
# A variável `prod` armazena o resultado do produto.
# O uso de `f-string` permite formatar a saída de forma clara e concisa. 