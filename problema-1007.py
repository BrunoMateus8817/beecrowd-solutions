# Problema 1007 - Diferença (Beecrowd)
# Este programa lê quatro inteiros, calcula a diferença entre os produtos e exibe o resultado

# Leitura dos quatro valores inteiros
A = int(input())
B = int(input())
C = int(input())
D = int(input())

# Cálculo da diferença
DIFERENCA = (A * B) - (C * D)

# Exibe o resultado
print(f"DIFERENCA = {DIFERENCA}") 