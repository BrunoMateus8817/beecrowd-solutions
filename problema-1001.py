A = int(input())
B = int(input())
X = A + B
print(f"X = {X}")
# Este é um programa Python que lê dois números inteiros, calcula a soma e imprime o resultado no formato "X = resultado".
# A função `input()` é usada para ler os números, e a função `print()` 
# exibe o resultado formatado. A variável `X` armazena a soma dos dois números.
# O uso de `f-string` permite incluir a variável diretamente na string de saída.
# O programa é útil para entender a entrada e saída de dados em Python, bem como a formatação de strings.
# É um exemplo básico de como realizar operações aritméticas simples e exibir resultados.