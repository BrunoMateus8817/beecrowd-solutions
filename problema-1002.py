raio = float(input())
n = 3.14159
area = n * raio ** 2
print(f"A={area:.4f}")
# Este programa calcula a área de um círculo dado o raio.
# A fórmula utilizada é A = π * r², onde π é aproximadamente 3.14159.
# O resultado é impresso com quatro casas decimais. 
# A função `input()` lê o valor do raio, e a função `print()` exibe o resultado formatado.
# O uso de `f-string` permite formatar o número com precisão específica. 